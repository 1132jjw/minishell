#include "libft.h"

int	ft_isalnum(int c)
{
	if ('0' <= c && '9' >= c)
		return (1);
	else if ('a' <= c && 'z' >= c)
		return (1);
	else if ('A' <= c && 'Z' >= c)
		return (1);
	return (0);
}
